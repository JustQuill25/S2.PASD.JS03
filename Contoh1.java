public class Contoh1 {
    private int nilai1;
    private int nilai2;

    // Constructor pertama (tanpa parameter)
    public Contoh1() {
        nilai1 = 0;
        nilai2 = 0;
    }

    // Constructor kedua (dengan parameter)
    public Contoh1(int a, int b) {
        nilai1 = a;
        nilai2 = b;
    }

    // Metode untuk mendapatkan nilai1
    public int getNilai1() {
        return nilai1;
    }

    // Metode untuk mendapatkan nilai2
    public int getNilai2() {
        return nilai2;
    }
}
